void fb_init(uint32_t addr, uint32_t width, uint32_t height, uint32_t pitch, uint8_t bpp) {
    fb.addr = addr;
    fb.width = width;
    fb.height = height;
    fb.pitch = pitch;
    fb.bpp = bpp;
    fb_clear(COLOR_BLACK);
}

static uint32_t color_to_pixel(color_t color) {
    if (fb.bpp == 32) {
        return ((uint32_t)color.r << 16) | ((uint32_t)color.g << 8) | (uint32_t)color.b;
    } else if (fb.bpp == 24) {
        return ((uint32_t)color.r << 16) | ((uint32_t)color.g << 8) | (uint32_t)color.b;
    } else if (fb.bpp == 16) {
        return (((color.r >> 3) << 11) | ((color.g >> 2) << 5) | (color.b >> 3));
    }
    return 0;
}

void fb_put_pixel(uint32_t x, uint32_t y, color_t color) {
    if (x >= fb.width || y >= fb.height) return;
    
    uint32_t pixel = color_to_pixel(color);
    uint32_t offset = y * fb.pitch + x * (fb.bpp / 8);
    uint32_t *addr = (uint32_t *)(fb.addr + offset);
    
    if (fb.bpp == 32) {
        *addr = pixel;
    } else if (fb.bpp == 24) {
        uint8_t *addr8 = (uint8_t *)(fb.addr + offset);
        addr8[0] = (pixel >> 16) & 0xFF;
        addr8[1] = (pixel >> 8) & 0xFF;
        addr8[2] = pixel & 0xFF;
    } else if (fb.bpp == 16) {
        uint16_t *addr16 = (uint16_t *)(fb.addr + offset);
        *addr16 = pixel;
    }
}

void fb_clear(color_t color) {
    fb_fill_rect(0, 0, fb.width, fb.height, color);
}

void fb_fill_rect(uint32_t x, uint32_t y, uint32_t w, uint32_t h, color_t color) {
    for (uint32_t yy = y; yy < y + h && yy < fb.height; yy++) {
        for (uint32_t xx = x; xx < x + w && xx < fb.width; xx++) {
            fb_put_pixel(xx, yy, color);
        }
    }
}

void fb_draw_char(uint32_t x, uint32_t y, char c, color_t fg, color_t bg) {
    const uint8_t *glyph = font_8x8[(unsigned char)c];
    
    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        for (int col = 0; col < 8; col++) {
            color_t pixel_color = (bits & (1 << (7 - col))) ? fg : bg;
            fb_put_pixel(x + col, y + row, pixel_color);
        }
    }
}

void fb_write_string(uint32_t x, uint32_t y, const char *str, color_t fg, color_t bg) {
    if (!str) return;
    
    uint32_t cur_x = x;
    for (int i = 0; str[i] != '\0'; i++) {
        if (str[i] == '\n') {
            y += 10;
            cur_x = x;
        } else {
            fb_draw_char(cur_x, y, str[i], fg, bg);
            cur_x += 8;
            if (cur_x + 8 > fb.width) {
                y += 10;
                cur_x = x;
            }
        }
    }
}

uint32_t fb_get_width(void) {
    return fb.width;
}

uint32_t fb_get_height(void) {
    return fb.height;
}
